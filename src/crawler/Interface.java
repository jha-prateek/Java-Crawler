    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String url = jTextField1.getText();
        String query = jTextField2.getText();
        int times = Integer.parseInt(jTextField3.getText());
        Spider spider = new Spider();
        spider.Crawl(query, times, url);
        jTextArea1.append("Frequency\tLink\n");
        for(int index=0; index<spider.wordsFound.size();index++)
        {
            jTextArea1.append(spider.wordsFound.get(index)+"\t"+spider.linksToVisit.get(index)+"\n");
        } 
        int max = Collections.max(spider.wordsFound);
        int index = spider.wordsFound.indexOf(max);
        try {
            URI uri= new URI(spider.linksToVisit.get(index));
            java.awt.Desktop.getDesktop().browse(uri);
            System.out.println("Opened in browser");

            } catch (Exception e) {
                   e.printStackTrace();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

